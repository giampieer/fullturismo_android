package principal.android.empresa.vr_proyecto.Bean;


public class LugaresBean {
    String idlugar;
    String nombre_lugar;
    String descripcion_lugar;
    String imagen_lugar;
    String itinerario_lugar;
    String nombre;
    String precio;
    String tipo_moneda;
    String dias;

    public String getIdlugar() {
        return idlugar;
    }

    public void setIdlugar(String idlugar) {
        this.idlugar = idlugar;
    }

    public String getNombre_lugar() {
        return nombre_lugar;
    }

    public void setNombre_lugar(String nombre_lugar) {
        this.nombre_lugar = nombre_lugar;
    }

    public String getDescripcion_lugar() {
        return descripcion_lugar;
    }

    public void setDescripcion_lugar(String descripcion_lugar) {
        this.descripcion_lugar = descripcion_lugar;
    }

    public String getImagen_lugar() {
        return imagen_lugar;
    }

    public void setImagen_lugar(String imagen_lugar) {
        this.imagen_lugar = imagen_lugar;
    }

    public String getItinerario_lugar() {
        return itinerario_lugar;
    }

    public void setItinerario_lugar(String itinerario_lugar) {
        this.itinerario_lugar = itinerario_lugar;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public String getTipo_moneda() {
        return tipo_moneda;
    }

    public void setTipo_moneda(String tipo_moneda) {
        this.tipo_moneda = tipo_moneda;
    }

    public String getDias() {
        return dias;
    }

    public void setDias(String dias) {
        this.dias = dias;
    }
}
